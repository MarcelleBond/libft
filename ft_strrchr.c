#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;
	int e;

	i = 0;
	e = -1;
	if (c == '\0')
	{
		while (s[i] != '\0')
			i++;
		return ((char *)&s[i]);
	}
	while (s[i] != '\0')
	{
		if (s[i] == c)
			e = i;
		i++;
	}
	if (e == -1)
		return (NULL);
	return ((char *)&s[e]);
}
